     public static String minDistance(double latitude,double longitude){
        double min = Double.POSITIVE_INFINITY;
        int index = 0;
        for(int i = 0;i<ids.length;i++){
           double d =  calculateDistance(latitudes[i],longitudes[i],latitude,longitude);
        if(min>d){
            min = d;
            index = i;
        }
    }

    return ids[index];
}

    private static double calculateDistance(double d1x,double d1y,double d2x,double d2y){
        double distance = Math.sqrt((d2x-d1x)*(d2x-d1x)+(d2y-d1y)*(d2y-d1y));

        return distance;
    }

